    private int generateUniqueUserId() throws SQLException {
        Random rand = new Random();
        int min = 11111;
        int max = 99999;
        int userId;
        UserAccess userAccess = new UserAccess(dbConnection.getConnection());
        do {
            userId = rand.nextInt(max - min + 1) + min;
        } while (userAccess.isUserIdExists(userId));

        return userId;
    }
    
