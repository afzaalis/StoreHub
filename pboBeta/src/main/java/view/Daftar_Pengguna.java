    private void Daftar_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Daftar_ButtonActionPerformed
        String username = Field_Nama.getText();
        String password = Field_Password.getText();
        String konPassword = Field_KonPassword.getText();
        double balance = 0;
        
        
         if (username.isEmpty() || password.isEmpty() || konPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pastikan isi semua data!.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            if (password.equals(konPassword)){
                int userId = generateUniqueUserId();
                User user = new User(userId, username, password, balance);
                UserAccess userAccess = new UserAccess(dbConnection.getConnection());
                userAccess.registerUser(user);
                JOptionPane.showMessageDialog(this, "Berhasil Daftar Pengguna Baru!");
                Field_Nama.setText("");
                Field_Password.setText("");
                Field_KonPassword.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Pastikan Password dan Konfirmasi Password sama");
                Field_Password.setText("");
                Field_KonPassword.setText("");
            }
          
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Gagal Dafter Pengguna Baru: " + ex.getMessage());
        }
    

    
    }//GEN-LAST:event_Daftar_ButtonActionPerformed

